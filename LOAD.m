classdef LOAD < handle
  %------------------------------------------------------------------------
  properties
    ID;
    nodeID;
    f = [];
  end
  %------------------------------------------------------------------------
  methods
    %----------------------------------------------------------------------
    function this = LOAD(ID, nodeID, f)
      this.ID = ID;
      this.nodeID = nodeID;
      this.f = f;
    end
    %----------------------------------------------------------------------
    function add(this, f)
      this.f = this.f + f;
    end
    %----------------------------------------------------------------------
    function write(this)
      fprintf('++ Load ID: %i\n', this.ID);
      fprintf('   Node ID: %i\n', this.nodeID);
      fprintf('   f: [%f; %f];\n', this.f(1), this.f(2));
      fprintf('++\n')
    end
    %----------------------------------------------------------------------
  end
  %------------------------------------------------------------------------
end